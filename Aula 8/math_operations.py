def addition(val1, val2):
    return val1 + val2

def subtraction(val1, val2):
    return val1 - val2

def multiplication(val1, val2):
    return val1 * val2

def division(val1, val2):
    return val1 / val2

if __name__ == "__main__":
    print(addition(2,6))
    print(subtraction(2,8))
    print(multiplication(3,7))
    print(division(4,2))